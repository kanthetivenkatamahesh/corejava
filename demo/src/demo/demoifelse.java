package demo;

public class demoifelse {

	public static void main(String[] args) {
       int x=10;       {
    	   if(x>=10)
    	   {
    		   	System.out.println("x is greater than 10");
    	   }
    	   else
    	   {
                System.out.println("x is smaller than 10");
    	   }
       }
	}
}
       
	


